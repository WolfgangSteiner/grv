#ifndef GRV_ALIGNMENT_H
#define GRV_ALIGNMENT_H

typedef enum {
    GRV_ALIGNMENT_NONE = 0,
    GRV_ALIGNMENT_HORIZONTAL_CENTER   = 1 << 0,
    GRV_ALIGNMENT_HORIZONTAL_LEFT     = 1 << 1,
    GRV_ALIGNMENT_HORIZONTAL_RIGHT    = 1 << 2,
    GRV_ALIGNMENT_HORIZONTAL_LEFT_OF  = 1 << 3,
    GRV_ALIGNMENT_HORIZONTAL_RIGHT_OF = 1 << 4,
    GRV_ALIGNMENT_HORIZONTAL_BLOCK    = 1 << 5,
    GRV_ALIGNMENT_VERTICAL_CENTER     = 1 << 6,
    GRV_ALIGNMENT_VERTICAL_TOP        = 1 << 7,
    GRV_ALIGNMENT_VERTICAL_BOTTOM     = 1 << 8,
    GRV_ALIGNMENT_VERTICAL_ABOVE      = 1 << 9,
    GRV_ALIGNMENT_VERTICAL_BELOW      = 1 << 10,
    GRV_ALIGNMENT_VERTICAL_BLOCK      = 1 << 11,
    GRV_ALIGNMENT_CENTER          = GRV_ALIGNMENT_HORIZONTAL_CENTER | GRV_ALIGNMENT_VERTICAL_CENTER,
    GRV_ALIGNMENT_TOP_LEFT        = GRV_ALIGNMENT_VERTICAL_TOP      | GRV_ALIGNMENT_HORIZONTAL_LEFT,
    GRV_ALIGNMENT_TOP_RIGHT       = GRV_ALIGNMENT_VERTICAL_TOP      | GRV_ALIGNMENT_HORIZONTAL_RIGHT,
    GRV_ALIGNMENT_TOP_CENTER      = GRV_ALIGNMENT_VERTICAL_TOP      | GRV_ALIGNMENT_HORIZONTAL_CENTER,
    GRV_ALIGNMENT_BOTTOM_LEFT     = GRV_ALIGNMENT_VERTICAL_BOTTOM   | GRV_ALIGNMENT_HORIZONTAL_LEFT,
    GRV_ALIGNMENT_BOTTOM_RIGHT    = GRV_ALIGNMENT_VERTICAL_BOTTOM   | GRV_ALIGNMENT_HORIZONTAL_RIGHT,
    GRV_ALIGNMENT_BOTTOM_CENTER   = GRV_ALIGNMENT_VERTICAL_BOTTOM   | GRV_ALIGNMENT_HORIZONTAL_CENTER,
    GRV_ALIGNMENT_ABOVE_LEFT      = GRV_ALIGNMENT_VERTICAL_ABOVE    | GRV_ALIGNMENT_HORIZONTAL_LEFT,
    GRV_ALIGNMENT_ABOVE_RIGHT     = GRV_ALIGNMENT_VERTICAL_ABOVE    | GRV_ALIGNMENT_HORIZONTAL_RIGHT,
    GRV_ALIGNMENT_ABOVE_CENTER    = GRV_ALIGNMENT_VERTICAL_ABOVE    | GRV_ALIGNMENT_HORIZONTAL_CENTER,
    GRV_ALIGNMENT_BELOW_LEFT      = GRV_ALIGNMENT_VERTICAL_BELOW    | GRV_ALIGNMENT_HORIZONTAL_LEFT,
    GRV_ALIGNMENT_BELOW_RIGHT     = GRV_ALIGNMENT_VERTICAL_BELOW    | GRV_ALIGNMENT_HORIZONTAL_RIGHT,
    GRV_ALIGNMENT_BELOW_CENTER    = GRV_ALIGNMENT_VERTICAL_BELOW    | GRV_ALIGNMENT_HORIZONTAL_CENTER,
    GRV_ALIGNMENT_LEFT_OF_CENTER  = GRV_ALIGNMENT_HORIZONTAL_LEFT_OF  | GRV_ALIGNMENT_VERTICAL_CENTER,
    GRV_ALIGNMENT_RIGHT_OF_CENTER = GRV_ALIGNMENT_HORIZONTAL_RIGHT_OF | GRV_ALIGNMENT_VERTICAL_CENTER,
    GRV_ALIGNMENT_LEFT_OF_TOP     = GRV_ALIGNMENT_HORIZONTAL_LEFT_OF  | GRV_ALIGNMENT_VERTICAL_TOP,
    GRV_ALIGNMENT_RIGHT_OF_TOP    = GRV_ALIGNMENT_HORIZONTAL_RIGHT_OF | GRV_ALIGNMENT_VERTICAL_TOP,
    GRV_ALIGNMENT_LEFT_OF_BOTTOM  = GRV_ALIGNMENT_HORIZONTAL_LEFT_OF  | GRV_ALIGNMENT_VERTICAL_BOTTOM,
    GRV_ALIGNMENT_RIGHT_OF_BOTTOM = GRV_ALIGNMENT_HORIZONTAL_RIGHT_OF | GRV_ALIGNMENT_VERTICAL_BOTTOM,
	GRV_ALIGNMENT_BLOCK           = GRV_ALIGNMENT_HORIZONTAL_BLOCK    | GRV_ALIGNMENT_VERTICAL_BLOCK,
} grv_alignment_t;

#endif
